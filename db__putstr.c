#include "libft/libft.h"

void	db__putstr(char const *c)
{
	int		i;

	i = 0;
	if (!c)
	{
		ft_putstr(ERR01);
		return ;
	}
	while (c[i])
	{
		ft_putchar(c[i]);
		i++;
	}
}
